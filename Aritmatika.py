X = 10
Y = 5
#  +
hasil = X + Y
print(X, "+", Y, "=", hasil)

# -

hasil = X - Y
print(X, "-", Y, "=", hasil)

# / 

hasil = X / Y
print(X, "/", Y, "=", hasil)

#  X
hasil = X * Y
print(X, "X", Y, "=", hasil)

# Eksponen 
hasil = X ** Y
print(X, "**", Y, "=", hasil)

# Modulus
hasil = X % Y
print(X, "%", Y, "=", hasil)

# Flow Division
hasil = X // Y
print(X, "//", Y, "=", hasil)

